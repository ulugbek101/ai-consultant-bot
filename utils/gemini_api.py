def _sync_call(history: list[dict]) -> str:
    messages = [{"role": "system", "content": _SYSTEM_PROMPT}] + history
    response = _client.chat.completions.create(
        model="gpt-4o-mini",
        messages=messages,
        max_tokens=1024,
    )
    return response.choices[0].message.content


async def get_ai_response(history: list[dict]) -> str:
    """history: list of {"role": "user"|"assistant", "content": str}"""
    return await asyncio.to_thread(_sync_call, history)


def build_history(db_rows: list[dict]) -> list[dict]:
    """Convert DB rows (role/content) to OpenAI-compatible history format."""
    result = []
    for row in db_rows:
        role = "assistant" if row["role"] == "assistant" else "user"
        result.append({"role": role, "content": row["content"]})
    return result
